<# bruteforce hash #>

# ── Validation ──
if (-not (Assert-RequiredVars @('HASHCAT','HASHTYPE','HASHES'))) { return }
if (-not (Assert-PathExists $HASHCAT 'HASHCAT exe')) { return }

# ── Select hashlist ──
$HASHLIST = Select-Hashlist -HashesRoot $HASHES -HashType $HASHTYPE
if (-not $HASHLIST) { return }

# ── Build base args ──
$argsBase = @()
Add-If -arr ([ref]$argsBase) $KERNEL
if ($KERNEL -and $KERNEL.Trim() -ne '') { $argsBase += '--bitmap-max=24' }
Add-If -arr ([ref]$argsBase) $HWMON
$modeToken = "-m${HASHTYPE}"

# ── Masks ──
$masks = @(
  "?a?a?a?a?a",
  "?l?l?l?l?l?l?l?l",
  "?u?u?u?u?u?u?u?u",
  "?d?d?d?d?d?d?d?d?d?d",
  @("-1","?l?d?u","?1?1?1?1?1?1?d?d"),
  @("-1","?l?u","-2","?d","?1?1?1?1?2?2?2?2?a"),
  @("-1","?d","-2","?l?u","?1?1?1?1?2?2?2?2"),
  "?l?l?l?l?l?d?d?d?d",
  "?u?u?u?u?u?d?d?d?d",
  "?l?l?l?l?l?l?d?d?d",
  "?u?u?u?u?u?u?d?d?d",
  "?d?d?d?d?d?l?l?l?l",
  "?d?d?d?d?d?u?u?u?u",
  "?l?l?d?d?d?d?d?d?d",
  "?u?u?d?d?d?d?d?d?d",
  "?l?l?d?d?d?d?l?l",
  "?u?u?d?d?d?d?u?u",
  "?l?d?d?l?d?d?l?d?d",
  "?u?d?d?u?d?d?u?d?d",
  "?d?d?d?d?d?d?d?d?l?l",
  "?d?d?d?d?d?d?d?d?u?u",
  "?d?d?l?d?d?l?d?d?l",
  "?d?d?u?d?d?u?d?d?u"
)

# ── Plan commands ──
$Planned = @()
for ($i = 0; $i -lt $masks.Count; $i++) {
  $maskArgs = Pattern-To-Args $masks[$i]
  $cmdArgs = @()
  $cmdArgs += $argsBase
  $cmdArgs += $modeToken
  $cmdArgs += $HASHLIST
  $cmdArgs += '-a3'
  $cmdArgs += $maskArgs
  $cmdArgs += '--increment'
  $cmdArgs = $cmdArgs | Where-Object { $_ -ne $null -and $_ -ne '' }
  $Planned += [pscustomobject]@{ Args = $cmdArgs }
}

# ── Execute ──
Invoke-PlannedCommands -Planned $Planned -HashcatExe $HASHCAT -Description 'mask bruteforce commands'
